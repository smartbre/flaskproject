def magicianprod(num1, num2):
    product = int(num1)*int(num2)
    return product
 
def magicianadd(num1, num2):
    addition = int(num1)+int(num2)
    return addition


def magiciansub(num1, num2):   
    subtraction = int(num1)*int(num2)
    return subtraction
    
